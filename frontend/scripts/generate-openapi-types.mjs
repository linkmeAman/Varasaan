import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const openApiPath = path.resolve(__dirname, "../../packages/shared/openapi/openapi.generated.json");
const outputPath = path.resolve(__dirname, "../src/api/openapi-types.ts");

if (!fs.existsSync(openApiPath)) {
  throw new Error(`OpenAPI artifact not found at ${openApiPath}`);
}

const raw = fs.readFileSync(openApiPath, "utf8");
const document = JSON.parse(raw);

const fileContent = `/* This file is auto-generated by frontend/scripts/generate-openapi-types.mjs. */\n\nexport const openApiDocument = ${JSON.stringify(
  document,
  null,
  2,
)} as const;\n\nexport type OpenApiDocument = typeof openApiDocument;\nexport type OpenApiPath = keyof OpenApiDocument["paths"];\n\nexport type OperationByPath<TPath extends OpenApiPath> = OpenApiDocument["paths"][TPath];\n`;

fs.mkdirSync(path.dirname(outputPath), { recursive: true });
fs.writeFileSync(outputPath, fileContent, "utf8");
console.log(`Wrote frontend API types to ${outputPath}`);

